package week02;

public class week02_2 {
    public static void main(String args[]){
        // 變數
        System.out.printf("%d ~ %d\n",Byte.MIN_VALUE,Byte.MAX_VALUE); //byte
        System.out.printf("%d ~ %d\n",Short.MIN_VALUE,Short.MAX_VALUE); //short
        System.out.printf("%d ~ %d\n",Integer.MIN_VALUE,Integer.MAX_VALUE); //int
        System.out.printf("%d ~ %d\n",Long.MIN_VALUE,Long.MAX_VALUE); //long
        // 浮點
        System.out.printf("%f ~ %f\n",Float.MIN_VALUE,Float.MAX_VALUE); //float
        System.out.printf("%f ~ %f\n",Double.MIN_VALUE,Double.MAX_VALUE); //double
        // 字元
        System.out.printf("%h ~ %h\n",Character.MIN_VALUE,Character.MAX_VALUE); //char
        // 布林
        System.out.printf("%b ~ %b\n",Boolean.FALSE,Boolean.TRUE); //boolen
    }
}
